// Claude Code adapter (design §3.4.1).
// Capability profile: nativeGoal, nativeHooks; no nativeSchedule → emit Scheduler.
// Emits: command, Stop hook, scheduler (crontab or GH Actions), CLAUDE.md block, loop.json.

import type { LoopIR } from "../ir/types.js";
import type { Adapter, CapabilityProfile, EmittedFile } from "./types.js";
import { paths } from "../paths.js";

const PROFILE: CapabilityProfile = {
  nativeGoal: true,
  nativeSchedule: false,
  nativeHooks: true,
  worktrees: true,
  headlessCmd: "claude -p",
  telemetry: "jsonl",
};

export const claudeCodeAdapter: Adapter = {
  target: "claude-code",
  capabilities: () => PROFILE,
  compile,
};

function compile(ir: LoopIR): EmittedFile[] {
  const files: EmittedFile[] = [
    emitCommand(ir),
    emitHook(ir),
    emitScheduler(ir),
    emitContextBlock(ir),
    emitLoopJson(ir),
  ];
  return files;
}

function emitCommand(ir: LoopIR): EmittedFile {
  const lines = [`# ${ir.name}`, "", ir.goal, "", "## Stop when", ir.stopCondition];

  if (ir.context.length > 0) {
    lines.push("", "## Context");
    for (const item of ir.context) lines.push(`- ${item}`);
  }

  return { path: paths.claudeCommand(ir.name), content: lines.join("\n") + "\n" };
}

function emitHook(ir: LoopIR): EmittedFile {
  const content = [
    "#!/bin/sh",
    `# Stop hook for the ${ir.name} loop — generated by loopmd build`,
    `exec loopmd guard --loop ${ir.name} --stdin`,
    "",
  ].join("\n");

  return { path: paths.claudeHook(ir.name), content, mode: 0o755 };
}

function emitScheduler(ir: LoopIR): EmittedFile {
  if (ir.schedule.kind === "event") return emitWorkflow(ir);
  if (ir.schedule.kind === "cron") return emitCrontab(ir);
  // manual: emit a crontab fragment with a placeholder
  return emitCrontab(ir);
}

function emitCrontab(ir: LoopIR): EmittedFile {
  const expr = ir.schedule.kind === "cron" ? ir.schedule.expr! : "# set your cron expression here";
  const content = [
    `# loopmd — ${ir.name}`,
    `# Add to crontab with: crontab -l | cat - crontab.d/${ir.name} | crontab -`,
    `${expr} loopmd run ${ir.name}`,
    "",
  ].join("\n");

  return { path: paths.crontab(ir.name), content };
}

function emitWorkflow(ir: LoopIR): EmittedFile {
  const event = ir.schedule.event ?? "push";
  const tokenLine = ir.budget.tokens !== undefined ? ` --tokens ${ir.budget.tokens}` : "";

  const content = [
    `name: loopmd — ${ir.name}`,
    `on:`,
    `  ${event}:`,
    `    branches: [main]`,
    ``,
    `jobs:`,
    `  loop:`,
    `    runs-on: ubuntu-latest`,
    `    steps:`,
    `      - uses: actions/checkout@v4`,
    `      - uses: actions/setup-node@v4`,
    `        with:`,
    `          node-version: '20'`,
    `      - run: npx loopmd run ${ir.name}${tokenLine}`,
    `        env:`,
    `          ANTHROPIC_API_KEY: \${{ secrets.ANTHROPIC_API_KEY }}`,
    ``,
  ].join("\n");

  return { path: paths.githubWorkflow(ir.name), content };
}

function emitContextBlock(ir: LoopIR): EmittedFile {
  if (ir.context.length === 0) {
    const block = `<!-- loopmd:start ${ir.name} -->\n<!-- loopmd:end -->\n`;
    return { path: paths.claudeContext, content: block, managed: ir.name };
  }

  const inner = [`## ${ir.name} loop context`, ...ir.context.map((c) => `- ${c}`)].join("\n");
  const block = `<!-- loopmd:start ${ir.name} -->\n${inner}\n<!-- loopmd:end -->\n`;
  return { path: paths.claudeContext, content: block, managed: ir.name };
}

function emitLoopJson(ir: LoopIR): EmittedFile {
  return {
    path: paths.loopConfig(ir.name),
    content: JSON.stringify(ir, null, 2) + "\n",
  };
}

// Build the human-readable run command used both in the scheduler output and by `loopmd run`.
export function buildRunCmd(ir: LoopIR): string {
  const tokenFlag = ir.budget.tokens !== undefined ? ` --tokens ${ir.budget.tokens}` : "";
  const isolationFlag = ir.isolation === "worktree" ? " --isolation worktree" : "";
  return `claude -p "/goal ${ir.stopCondition}"${tokenFlag}${isolationFlag}`;
}
